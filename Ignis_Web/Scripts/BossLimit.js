$('#FirstFull').change(function () {

    if ($(this).is(':checked')) {
        $('#First1').prop('disabled', true);
        $('#First2').prop('disabled', true);
        $('#First3').prop('disabled', true);
        $('#First4').prop('disabled', true);
        $('#First5').prop('disabled', true);

        $('#First1').prop('checked', false);
        $('#First2').prop('checked', false);
        $('#First3').prop('checked', false);
        $('#First4').prop('checked', false);
        $('#First5').prop('checked', false);

    }
    else {
        $('#First1').prop('disabled', false);
        $('#First2').prop('disabled', false);
        $('#First3').prop('disabled', false);
        $('#First4').prop('disabled', false);
        $('#First5').prop('disabled', false);
    }
});

